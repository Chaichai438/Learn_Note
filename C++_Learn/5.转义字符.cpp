#include<iostream>

using namespace std;

int main()
{
    cout << "Hello World!!!\n";
    cout << "\\" << endl;                           //   "\\"=“\"
    cout << "aaa\t\tHello World!!!" << endl;        // \t 水平制表符，可以整齐的输出数据，一个代表着8空格
    cout << "a\tHello World!!!" << endl;
    cout << "aaaaa\tHello World!!!" << endl;

    return 0;

}
